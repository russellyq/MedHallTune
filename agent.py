API_KEYS = 'Your Own API Key Here'  # Please replace with your own API key

class Evaluator:
    def __init__(self, api_key=API_KEYS):
        self.http_client = httpx.AsyncClient(
            proxy="http://proxy.cse.cuhk.edu.hk:8000/",
            timeout=60.0,
        )
        self.client = AsyncOpenAI(api_key=api_key,
                                #  base_url='https://api.302.ai/v1/chat/completions',
                                 http_client=self.http_client
                                 )

    async def evaluate(self, messages):
        response = await self.client.chat.completions.create(
            model="gpt-4o", # OpenAI Model
            messages=messages
        )
        return response.choices[0].message.content
    async def aclose(self):
        await self.http_client.aclose()


def wrap_eval_message(sample):
    fig_caption = sample.get("fig_caption")
    question = sample["question"]
    ans = sample["answer"]
    gt_answer = sample["gpt4_answer"]
    reference_answers = sample["reference_answers"]
    reference_scores = sample["reference_scores"]

    conv_to_str = (
        f"Figure Caption: {fig_caption}\n\n"
        f"[Question]: {question}\n\n"
        f"[Answer]: {ans}\n [End of Answer]\n\n"
        f"[Ground Truth Answer]: {gt_answer}\n [End of Ground Truth Answer]\n\n"
        f"[System]: {INSTRUCTION_PROMPT}\n\n"
    )

    messages = [
        {"role": "system", "content": "You are a helpful and precise assistant for checking the quality of the answer."},
        {"role": "user", "content": conv_to_str},
        {"role": "user", "content": f"Sample Answer: {reference_answers}\n\nSample Scores: {reference_scores}\n\n"},
    ]
    return messages

async def dispatch_evaluation_requests(samples, evaluator: Evaluator):
    message_list = [wrap_eval_message(s) for s in samples]
    tasks = [evaluator.evaluate(m) for m in message_list]
    return await asyncio.gather(*tasks)


def call_eval_async(samples):
    async def _runner():
        evaluator = Evaluator()
        try:
            return await dispatch_evaluation_requests(samples, evaluator)
        finally:
            await evaluator.aclose()

    predictions = None
    for retry in range(2):
        try:
            predictions = asyncio.run(_runner())
            break
        except Exception as e:
            print(f"Error in call_eval_async (attempt {retry+1}): {e}")
            if retry < 1:
                import time
                time.sleep(1)

    results = []
    if predictions is None:
        return results

    for sample, prediction in zip(samples, predictions):
        if prediction:
            sample["result"] = prediction
            results.append(sample)
    return results